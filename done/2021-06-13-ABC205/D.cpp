int main(){
  ll n,q;
  cin >> n >> q;
  vll v;
  rep(n){
    ll a;
    cin >> a;
    v.pb(a);
  }
  sort(ALL(v));
  rep(q){
    ll k;
    cin >> k;
    ll ng = 0;
    ll ok = INF;
    while(1){
      ll md = (ng+ok)/2;
      auto iter = lower_bound(ALL(v),md);
      ll kyori = iter - v.begin();
      if(*iter != md && md - kyori == k){
        cout << md << endl;
        break;
      }
      if(md - kyori > k){
        ok = md;
      }
      else ng = md;
    }
  }
  return 0;
}
